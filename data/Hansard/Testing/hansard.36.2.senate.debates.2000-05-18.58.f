Deuxieme lecture-Suite du debat 
Au Senat, nous sommes tous captifs des horaires des compagnies aeriennes.  
Je vais donc traiter de ce projet de loi visant a proteger les phares patrimoniaux. 
Les gens tiennent beaucoup aux phares et cela, sur les deux cotes. 
Les phares gardes temoignent vraiment de la securite de nos cotes. 
Ce projet de loi permettra la creation de partenariats financiers.  
Le temps commence a manquer, nombre de ces phares necessitant de toute urgence des reparations. 
Les dispositions actuelles regissant la protection des phares sont insuffisantes. 
D'autres phares ont ete detruits sans le moindre avis.  
