Apres cet accident, le phare de Brockton Point a ete ameliore. 
Notre famille soutient que Brockton Point lui appartient. 
Il est situe sur la rive ouest et est entre en activite en 1874. 
Pour tous ses efforts, sa pension se chiffrait a un maigre 33 $ par mois.  
Dans mon ile de Saturna, notre phare n'a pas la designation patrimoniale. 
Le phare a ete etabli en janvier 1888.  
Les habitants de Saturna Island l'adorent. 
