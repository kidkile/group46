Un autre phare, celui de Fisgard, a ete construit en 1860. 
Il devait signaler la proximite du port de Victoria. 
C'est une merveille architecturale. 
De nos jours, le phare fait partie du parc historique national Fort Rodd Hill.  
Le navire a heurte les rochers battus par d'enormes vagues.  
La plupart ont peri.  
