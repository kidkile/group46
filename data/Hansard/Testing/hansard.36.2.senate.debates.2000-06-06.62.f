Projet de loi sur la citoyennete au Canada 
Deuxieme lecture-Ajournement du debat 
C'est plutot la reflexion des valeurs que nous avons en commun. 
La premiere loi sur la citoyennete canadienne, adoptee en 1947, exprimait ces memes valeurs. 
La loi de 1947 refletait les moeurs et les attitudes de l'epoque. 
Cependant, elle ne definit pas ce que signifie le mot residence. 
La presence effective sera determinee de diverses facons. 
Le nombre de 1 095 jours est raisonnable. 
Je crois que c'est une mesure logique.  
L'adoption est une competence provinciale et territoriale, comme on le sait. 
L'adoption doit etre dans l'interet de l'enfant. 
Toute adoption doit creer un lien de filiation authentique. 
Les changements contenus dans le projet de loi C-16 refletent la situation actuelle. 
Le changement au processus d'adoption sera retroactif a toute adoption faite apres 1977.  
Il s'agit d'une tache fastidieuse. 
Ils feront activement la promotion de la citoyennete. 
Les commissaires seront encore nommes par le gouverneur en conseil. 
Les commissaires fourniront aussi des conseils utiles au ministre sur les questions de citoyennete. 
Le projet de loi C-16 modernise la citoyennete canadienne. 
La criminalite, la fausse identite ou l'identite criminelle sont les principales causes d'annulation. 
La revocation de la citoyennete est une question serieuse. 
Ce n'est tout simplement pas le cas. 
Il existe 15 causes qui ont prouve la legalite de la procedure.  
Incidemment, ce meme processus est applique dans presque tous les pays du Commonwealth. 
Honorables senateurs, le processus de revocation fait fond sur nos traditions parlementaires. 
Le pouvoir de revoquer la citoyennete doit donc revenir egalement a l'executif.  
Je ne pense pas que ce soit conforme a la facon de faire des Canadiens. 
La citoyennete canadienne n'est pas un droit, mais un privilege. 
La derniere question que je voudrais traiter aujourd'hui concerne les peines. 
Honorables senateurs, la plupart d'entre nous sommes des immigrants. 
