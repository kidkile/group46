Rapport du Comite des affaires sociales, des sciences et de la technologie-Ajournement du debat 
Permission ayant ete accordee de revenir aux Rapports de comites: 
Cette strategie nationale constitue la premiere de nos14 recommandations figurant dans le rapport. 
Les Canadiens veulent que le gouvernement federal joue un role de leader.  
Qu'en est-il de ceux qui souffrent de diverses autres maladies des poumons? 
Pourquoi ne beneficient-ils pas de soins palliatifs? 
Honorables senateurs, des Canadiens souffrent encore inutilement avant de mourir. 
Certains progres ont ete realises toutefois. 
Toutefois, il y a des choses que le gouvernement federal peut faire seul. 
Nous avons propose au fil des ans l'adoption d'un conge parental. 
Nous avons aussi propose l'adoption d'un conge de maternite. 
On creera de 12 a 15 de ces instituts de recherche au Canada. 
Eh bien, elles ne donnent toujours pas de formation en la matiere aux futurs medecins. 
Dans notre pays, il n'existe toujours pas de residence en soins palliatifs. 
Ils avaient essaye de creer un programme. 
Je le repete, c'est une question d'argent. 
Les fonds ne sont tout simplement pas la.  
Honorables senateurs, ce fut un honneur pour moi de copresider ce sous-comite. 
Le senateur Corbin a ete des notres pendant tous les temoignages. 
J'aimerais feliciter tout particulierement le senateur Roche. 
Sans examen TDM ou IRM, c'est la mort. 
Je dis toujours qu'un lexique est une des choses les plus importantes. 
Si nous definissons les termes au debut, les discussions traineront moins en longueur. 
Cela est tres avantageux.  
Je repete en terminant que le present rapport est unanime. 
Cela est tres important. 
Comme il est dit dans notre rapport, c'est impardonnable.  
Soyons clairs a cet egard, honorables senateurs. 
L'euthanasie et l'aide au suicide sont illegales au Canada, et devraient le rester. 
La maladie, la souffrance et la mort font inevitablement partie de l'experience humaine. 
C'est peut-etre a cause du deni de la mort omnipresent dans notre culture. 
Les medecins sont formes pour soigner les gens et les aider a se sentir mieux. 
La technologie nous offre sans cesse de nouveaux moyens de prolonger la vie. 
La justice sociale exige que le gouvernement accorde une attention prioritaire aux soins palliatifs. 
