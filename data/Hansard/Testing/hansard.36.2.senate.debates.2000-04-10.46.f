Deuxieme lecture-suite du debat 
Honorables senateurs, notre Constitution de 1867 est muette sur la secession d'une province. 
Toute disposition de la Constitution peut etre modifiee. 
Notre Constitution est aussi muette sur la question des referendums. 
Le Parlement et les provinces peuvent adopter des lois a cet egard. 
Un referendum est facultatif dans notre systeme.  
La reponse a ete non dans les deux cas. 
C'est probablement plus impressionnant, mais c'est certainement beaucoup moins flexible.  
Cette decision est prise par une resolution. 
Le pouvoir legislatif est prevu, decrit et defini dans la Constitution. 
Certains pouvoirs sont exclusifs et quelques-uns sont partages.  
Ce qui est supreme, dans notre pays, c'est la Constitution. 
Depuis 1982, nous avons cinq formules de modification. 
Trois points dans le projet de loi me paraissent porter flanc a la critique. 
L'esprit du federalisme est-il respecte? 
Le Senat est une Chambre legislative au meme titre que la Chambre des communes. 
Au palier federal, le Parlement est compose de deux Chambres. 
En matiere d'adoption des lois, les deux Chambres sont egales. 
Il s'agit d'une loi.  
Ainsi le veulent les principes fondamentaux de notre democratie parlementaire. 
Ce n'est pas mon avis. 
Le projet deloi C-20 laisse le vote de confiance intact. 
Il n'y touche meme pas, et c'est bien. 
Bien sur, le vote de confiance n'existe qu'a la Chambre des communes. 
Bien sur, il fait partie de nos conventions constitutionnelles depuis 1847, sinon 1846. 
Dans ce cas, c'etait l'article 133 de la Constitution. 
Au contraire, l'adoption d'une resolution fait partie du processus legislatif. 
A mon avis, le projet de loi C-20 devrait etre modifie en consequence.  
C'est vrai, le Senat n'a qu'un veto suspensif. 
C'est un point important, mais on l'a fait par un amendement constitutionnel. 
On ne l'a pas fait par une simple loi. 
Honorables senateurs, pourquoi devrions-nous accepter une telle erosion de nos pouvoirs? 
Je suis un partisan de la clarte. 
J'ai toujours souhaite la clarte. 
La Cour supreme, dans son avis, l'a clairement envisage au paragraphe 155 du renvoi. 
La loi impressionne, mais elle n'est pas flexible.  
Le plan A debouche sur l'esperance. 
J'espere qu'on y reviendra. 
Le plan A est base sur un fil conducteur. 
Bref, je m'eleve contre l'erosion de nos pouvoirs. 
Nous vivons peut-etre notre derniere heure.  
Par contre, est-ce la bonne formule? 
Je ne debattrai pas le role du Senat, c'est un probleme constitutionnel. 
Je ne veux pas juger notre cour. 
Une question se pose, et je cite encore Patrick Monahan: 
Quelque chose doit manquer a notre societe pour que l'on agisse de la sorte. 
J'espere que la loi actuelle n'y mettra pas d'entrave.  
(Sur la motion du senateur Carstairs, au nom du senateur Pitfield, le debat est ajourne.)  
