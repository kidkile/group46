L'avenir de la BFC Shilo 
Honorables senateurs, ma question s'adresse au leader du gouvernement au Senat. 
De plus, le gouvernement a depense 3 millions de dollars pour ameliorer les installations sportives. 
Le Manitoba a effectivement un desert et Shilo y est situe. 
C'est ce que rapporte le Free Press de Winnipeg. 
J'aimerais obtenir une reponse.  
L'honorable J. Bernard Boudreau (leader du gouvernement) :
Cependant, il semble certes exister d'importantes infrastructures. 
La possibilite de la fermeture de la base de Kapyong 
Honorables senateurs, il y a une autre base au Manitoba appelee Kapyong. 
C'est la qu'est installe le Princess Patricia's Canadian Light Horse Infantry. 
Cette base est tres vieille. 
Elle a ete construite pendant et apres la Seconde Guerre mondiale. 
Comparee a la base de Shilo, elle est vraiment tres delabree. 
Le terrain sur lequel est construite la base a une tres grande valeur. 
