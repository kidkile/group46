Projet de loi modificatif-Deuxieme lecture-Ajournement du debat 
Cela me semble simple. 
Le client peut donner son consentement informe de diverses facons.  
De meme, le projet de loi prevoit certaines exceptions. 
Il protege le droit fondamental du consommateur de choisir. 
Le projet de loi C-6 a rendu cela possible.  
Cette inversion de la relation normale entre vendeur et acheteur repose sur un consentement implicite. 
C'est une pratique de commercialisation trompeuse, mais tres lucrative. 
La proportion est particulierement elevee chez les personnes de plus de 65 ans. 
Elles sont encore plus vulnerables et exposees a de plus grands risques.  
Les senateurs trouvent-ils cela juste? 
En fait, ils avaient achete de l'assurance sans avoir demande quoi que ce soit. 
Songez aux CPG, que nous sommes nombreux a avoir. 
Les clients avaient jusqu'en octobre 1997 pour faire part de leur preference. 
S'ils ne se prononcent pas, la Banque conclura que les informations devraient etre partagees.  
Nous attendons des banques qu'elles fassent preuve du comportement moral le plus strict.  
Un autre exemple qui m'a ete signale concerne des etudiants universitaires. 
La compagnie ne les rappelle jamais pour savoir s'ils sont satisfaits. 
Elle ne rappelle jamais pour savoir s'ils veulent les services en question. 
La compagnie de telephone a donc utilise la methode de l'abonnement par defaut.  
J'en veux pour preuve le fiasco de l'abonnement par defaut de janvier 1995. 
D'aucuns diront que les cablodistributeurs ont eu leur lecon. 
Vous etes un organisme de surveillance et de protection des consommateurs au Quebec. 
Elles ne se soumettent pas a la Loi sur la protection du consommateur.  
Elle a fait allusion au lancement de nouvelles chaines specialisees par Videotron en 1997: 
C'est ce qu'elle avait a dire. 
Il est evident que le depute bloquiste de Temiscamingue est d'accord. 
C'est pourquoi il y a eu des plaintes.  
Il vise aussi a garantir le maintien d'une liberte de choix sur le marche. 
Entre-temps, les entreprises s'emplissent les poches.  
C'est simple, rentable et efficace, mais cela n'est pas acceptable pour autant. 
En fait, on peut faire valoir que la commercialisation par abonnement par defaut est anticoncurrentielle. 
Cela peut mener a davantage de concentration sur le marche. 
Je ne vois pas en quoi l'abonnement par defaut peut favoriser la concurrence. 
Il n'y a pas de choix.  
Je reconnais que c'est peut-etre legitime. 
Une modification a ete apportee pour tenir compte de l'evolution du commerce electronique. 
En depit des changements, les elements cles de la protection du consommateur sont restes inchanges. 
Honorables senateurs, le projet de loi modifie la Loi sur la concurrence. 
Il n'interdit pas l'augmentation des frais de service exiges par les banques. 
Je pense ici a Rogers et a Cogeco.  
Le consentement expres peut etre donne de plusieurs manieres. 
A cet egard, le projet offre beaucoup de souplesse.  
Au total, les grandes banques ont plus de 20 millions de clients, nous dit-on. 
Il me semble que c'est loin d'etre le cas. 
Avec seulement six banques pour 20 millions de clients, y a-t-il reellement concurrence? 
