La Journee internationale de la femme 
L'honorable Erminie J. Cohen :
Honorables senateurs, hier, nous avons observe la Journee internationale de la femme. 
Combien d'histoires de courage et d'abnegation pourraient nous inspirer!  
C'etait durant la terrible epidemie de petite verole, en 1639. 
Cette generosite de coeur s'est maintenue jusqu'a ce jour. 
Les soins infirmiers des annees 90 sont fort complexes. 
C'est une decision tres positive.  
L'honorable Therese Lavoie-Roux :
Les femmes agees sont parmi les plus pauvres au Canada. 
Les mauvais traitements sont un autre probleme auquel les femmes agees sont confrontees. 
Honorables senateurs, des milliers de femmes agees au Canada ne beneficient pas de ces droits. 
Les femmes agees risquent d'etre abusees et de vivre dans la pauvrete. 
Honorables senateurs, cette semaine marque la derniere Journee internationale de la femme du siecle. 
Certains expriment aussi de la consternation a la suite de cette decision. 
Cependant, les remarques de M. Greenspan sont encore plus troublantes. 
Le jugement de madame la juge L'Heureux-Dube etait ni irresponsable, ni particulierement ideologique. 
Il etait plutot le reflet du gros bon sens. 
Le mot feministe est-il devenu pejoratif? 
Prenons le cas du Pakistan, par exemple.  
Thomas Jefferson a dit que le prix de la justice est l'eternelle vigilance. 
Poussee a l'extreme, une telle attitude excuserait la plupart des agressions sexuelles.  
Nous lui devons respect et gratitude. 
A cette occasion, aucune critique n'avait ete formulee a l'endroit du juge.  
