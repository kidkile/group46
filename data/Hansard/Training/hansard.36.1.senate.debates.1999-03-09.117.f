Le deces de m. Joe dimaggio 
L'honorable Francis Mahovlich :
Honorables senateurs,Joe DiMaggio a atteint le marbre. 
Il etait l'une des legendes de l'Amerique et du baseball. 
J'ai eu l'occasion de le rencontrer il y a quelques annees. 
Je l'ai trouve interessant et j'ai commence a le lire. 
Moe Berg etait un Americain qui jouait pour les Yankees de New York. 
Je suppose que l'armee voulait des renseignements sur ces villes. 
C'etait Moe Berg.  
J'ai trouve cette histoire tres amusante.  
Nous etions tres excites a l'idee d'y aller. 
Mon epouse avait sorti son gant de baseball. 
Bien entendu, elle a eu un oeil au beurre noir. 
Quand nous sommes arrives a Atlantic City, elle portait des lunettes de soleil. 
La legende a disparu, mais les souvenirs restent.  
