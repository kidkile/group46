Le deces de l'honorable William J. Petten 
L'honorable B. Alasdair Graham (leader du gouvernement) :
Il a compris et applique l'une des plus belles notions humaines - le leadership. 
Il savait qu'il est souvent necessaire de suivre pour pouvoir mieux diriger. 
Il y a 50 ans, Bill Petten a choisi de devenir Canadien. 
Il etait ce genre d'homme, ce genre d'ami. 
La magnifique Ode a Terre-Neuve rend bien toute la magie de: 
Nous adressons leurs prieres au ciel Que Dieu te garde, Terre-Neuve...  
Que Dieu te garde, Bill Petten. 
L'honorable C. William Doody :
Bill etait un courtier en douanes, comme on disait a l'epoque. 
Il m'a demande de parler de ses passe-temps favoris et de ses interets. 
Ils etaient sa vie. 
Bill Petten a ete appele au Senat en 1968. 
Il a ete whip liberal au Senat pendant 17 ans. 
Tous ici savent la tache ingrate et difficile qu'est celle de whip. 
Le senateur Graham a parle de son epouse Bernice, une femme merveilleuse et forte. 
Il est extremement difficile d'imaginerBill Petten sans Bernice. 
Ils formaient tout une equipe. 
Je sais a quel point il lui manquera. 
Bill a ete mon mentor au Senat. 
Il en a toujours ete un element important jusqu'a la fin.  
Bill a toujours ete un fier defenseur de sa province natale. 
L'honorable Orville H. Phillips :
J'ai siege au Senat aux cotes de Bill et de son pere. 
Nous sommes devenus des amis bien speciaux dans des circonstances assez comiques. 
Il a eu l'air un peu surpris que je ne sois pas d'accord. 
En notre qualite de whips, nous avions souvent de longues discussions. 
Sur la recommandation du docteur Keon, nous buvions tous les deux du the decafeine. 
Nous avions de nombreuses discussions amicales a ce sujet.  
Il aimait le rocher, comme il appelait Terre-Neuve. 
Il aimait ses compatriotes et il les comprenait. 
Ses histoires me faisaient bien rire.  
L'honorable P. Derek Lewis :
Bill et moi avons ete des amis pendant environ 70 ans. 
Depuis lors, nous avons partage nos experiences de vie, les hauts comme les bas. 
Je puis assurer aux senateurs qu'il etait un ami exceptionnel.  
Je me suis rejoui lorsqu'il a ete nomme au Senat en 1968. 
A mon arrivee au Senat en 1978, Bill etait whip de mon parti. 
Il s'y appliquait avec serieux et succes. 
Il y parvenait grace a sa forte personnalite, son intelligence et son ardeur au travail. 
Ses amis etaient legion.  
Ce faisant, il s'est fait des amis partout dans le monde. 
Il n'y a jamais menage sa peine.  
Nous avons tous perdu un excellent ami et ex-collegue, sur lequel on pouvait compter. 
Je le regretterai beaucoup.  
Il accueillait volontiers tous les visiteurs, d'ou qu'ils viennent.  
Bill Petten etait un authentique gentleman, plein de bienveillance. 
Nous avons alors habite chez les Petten. 
Ils se sont occupes de nous pendant quelques jours. 
Il etait un menuisier, un homme a tout faire. 
Il pouvait et aimait travailler de ses mains. 
Il faisait volontiers ce genre de travaux pour autrui.  
Cela nous arrive a l'occasion. 
Nous allons meme parfois jusqu'a nous mettre a chanter. 
Nous amorcions la soiree en chantant nos chansons, puis passions aux balades irlandaises. 
Voila le souvenir que je garderai de lui. 
Je me souviendrai aussi des histoires qu'il nous racontait. 
Il avait un nombre infini d'histoires et d'anecdotes.  
Il me manquera enormement. 
J'offre mes sympathies a Bernice ainsi qu'a Rob, Sharon, Raylene et Billy.  
