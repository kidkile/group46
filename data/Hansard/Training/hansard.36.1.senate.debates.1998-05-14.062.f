L'honorable Philippe Deane Gigantes :
Les pequistes font la meme chose, mais nous ne sommes pas comme les pequistes. 
J'ai deja dit a une personne qu'elle avait remporte un concours. 
Elle m'a repondu: Mais, senateur, saviez-vous que je suis separatiste? 
Je lui ai dit: Je vous embauche. 
Elle a ete loyale envers moi. 
C'est cela, la realite canadienne, l'ideal canadien. 
S'attaquer a quelqu'un a cause de ses convictions politiques est indigne.  
