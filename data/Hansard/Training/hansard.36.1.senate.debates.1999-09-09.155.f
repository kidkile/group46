Meme les enfants ont ete menottes de fer. 
Des fillettes de 12 ans ont ete soumises a des fouilles a nu. 
De jeunes enfants ont ete inutilement separes de leur mere en detention. 
Nos lois et reglements en matiere d'immigration sont empreints d'equite et de compassion. 
Nous donnons l'occasion a ceux qui demandent de l'aide de presenter leur cas.  
Ces derniers cherchaient tout simplement a ameliorer leur sort. 
Notre pays a grandi grace a la migration economique. 
L'immigration constitue un investissement en capital humain. 
Honorables senateurs, j'espere que la bonne volonte des Canadiens prevaudra. 
