L'honorable Jerahmiel S. Grafstein :
John et moi faisions partie de la promotion de 1958. 
Bora Laskin enseignait le droit du travail, le droit immobilier et le droit constitutionnel. 
J.B. Milner enseignait le droit des obligations contractuelles. 
John Sopinka etait vif de corps et encore plus vif d'esprit. 
Il etait issu d'un groupe minoritaire. 
Il prenait instinctivement et spontanement la defense des defavorises. 
Il avait un grand esprit de competition. 
L'Histoire sera un meilleur juge des consequences de son action.  
John avait aussi un humour tres malicieux. 
Puis, il demolissait la reponse de l'etudiant avec son exegese critique. 
John prit bravement son tour et affronta la tempete. 
Plusieurs semaines plus tard, la meme scene se repeta. 
Content de faire votre connaissance. 
Le deces de John Sopinka est une tragedie. 
Il a ete fauche a l'apogee d'une brillante carriere judiciaire. 
Deux seules consolations demeurent. 
Notre tour a tous viendra selon la terrifiante grace de Dieu. 
La petite consolation est que John ne sera jamais oublie. 
