Le developpement des ressources humaines- L'excedent du fonds de l'assurance-emploi 
Il a declare ce qui suit: 
Nos audiences se sont etalees du 22 octobre au 4 novembre 1997. 
En dix journees de travail, nous avons entendu plus de 60 temoins et groupes. 
Nous avons egalement recu des memoires ecrits de la part de pres de 70 personnes.  
Ces temoins etaient tous favorables aux commissions scolaires linguistiques. 
Ils tiennent essentiellement a l'egalite, au pluralisme et a l'absence de discrimination. 
Autrement dit, la democratie et le choix parental ont prevalu.  
A cet egard, nous avons fait appel a plusieurs experts en droit constitutionnel. 
Son Honneur le President :
Honorables senateurs, nous examinons encore une fois une resolution constitutionnelle. 
Ces audiences s'imposaient vu qu'a Quebec, aucune audience parlementaire n'a eu lieu. 
C'etait deja un consensus important dont nous avons tenu compte. 
Ce travail, nous l'avons fait.  
Des points de vue varies ont ete entendus. 
Je voudrais rapidement faire quelques commentaires sur certains points. 
Mon premier commentaire porte sur l'article 43.  
Nous avons cinq formules d'amendement constitutionnel dans la Loi constitutionnelle de 1982. 
Tous les experts sont d'accord sur ce point. 
Le texte anglais a l'article 43 est plus clair que le texte francais. 
Au Quebec, seule la disposition liminaire va demeurer. 
L'inverse, toutefois, n'est pas stipule a l'article 93. 
Mon deuxieme commentaire porte sur l'article 93 actuel. 
L'article 93 repondait aux besoins de 1867. 
Tout le monde ou presque etait alors protestant ou catholique. 
Mon troisieme commentaire porte sur l'enseignement religieux. 
Ce debat n'existe pas qu'au Quebec ou a Terre-Neuve ou au Canada. 
Il se deroule dans plusieurs de nos grandes democraties. 
Il est normal dans nos Etats modernes que l'Etat legifere en matiere d'education. 
C'est l'une de ses grandes responsabilites. 
Il suffit d'une proclamation de l'Assemblee nationale. 
Il revient ici au Quebec de decider.  
