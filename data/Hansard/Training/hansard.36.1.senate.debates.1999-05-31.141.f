L'honorable Consiglio Di Nino :
Au Canada, nous vivons dans une societe libre et democratique. 
Nous jouissons de la liberte de conscience, de religion, d'expression et d'association. 
Nous avons droit a l'application reguliere de la loi et a un proces juste. 
Je ne cherche pas a etre moralisateur. 
Je sais que nous ne sommes pas parfaits. 
Toutefois, les Canadiens ont au moins droit a l'application reguliere de la loi.  
Parlant des massacres de 1989, M. Chan a dit ceci a un auditoire de Vancouver: 
La dignite et la vie humaine sont les biens les plus precieux d'un pays. 
Aucun gouvernement ne peut justifier de tuer ses propres administres.  
Je ne saurais etre plus d'accord.  
J'espere que nous entendrons davantage parler de lui et de ses collegues. 
Certains d'entre nous continueront le combat en leur nom. 
