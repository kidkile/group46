L'efficacite des programmes visant a aider les agriculteurs 
L'honorable Leonard J. Gustafson :
Ma question porte sur un sujet qu'il connait bien. 
Les agriculteurs de la Nouvelle-Ecosse ont subi trois annees de secheresse. 
Le programme que le gouvernement a mis sur pied ne les a pas aides. 
Honorables senateurs, 70 p. 100 de rien, c'est zero. 
Le leader du gouvernement est-il au courant de cela? 
Dans l'affirmative, que fera-t-il a cet egard?  
Les agriculteurs sont ici a Ottawa depuis trois jours. 
Ils connaissent une situation de crise au Canada. 
L'honorable J. Bernard Boudreau (leader du gouvernement) :
Honorables senateurs, je remercie le ministre de sa reponse. 
Le probleme, c'est que le programme ACRA ne fonctionne pas. 
L'agriculteur qui a connu trois mauvaises annees de suite est vraiment en difficulte. 
Le programme ne se declenchera pas dans son cas. 
C'est la un des principaux problemes. 
C'est la un sujet de preoccupation national. 
Le pays ne peut se permettre de perdre ses agriculteurs. 
Le probleme revet une telle urgence qu'il appelle une intervention immediate.  
Des groupements professionnels agricoles ont cherche a rejoindre le premier ministre. 
Ils lui ont lance du ble a la figure. 
Toutefois, il s'est effectivement presente.  
Honorables senateurs, il me fera plaisir de transmettre ce message au nom du senateur Gustafson.  
