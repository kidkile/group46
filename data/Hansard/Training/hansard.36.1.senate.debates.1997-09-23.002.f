Son Honneur le President du Senat repond en ces termes: 
Honorables senateurs et senatrices, 
Mesdames et Messieurs les deputes , 
Mes fonctions m'amenent a rendre visite a de nombreuses unites des Forces canadiennes. 
Le 2 juin 1997, les Canadiens ont renouvele le mandat du gouvernement. 
Plus que jamais, les Canadiens veulent que leurs gouvernements travaillent en partenaires.  
...Vers un nouveau siecle de realisations canadiennes 
Le succes nous attend. 
Nos citoyens ont les qualites requises pour reussir au XXIe siecle: 
Nous avons le sens du partage et de l'entraide.  
Nous sommes innovateurs et ouverts aux idees nouvelles.  
Nous sommes une societe ouverte et democratique.  
Nous sommes un pays bilingue et multiculturel a une epoque de mondialisation croissante.  
Notre federation tire le meilleur parti possible de ces talents. 
Nous avons deja jete les bases de notre succes 
Il equilibrera le budget au plus tard durant l'annee financiere 1998-1999.  
Au Canada, un emploi sur trois est tributaire du commerce. 
Les Canadiens regagnent confiance en leur avenir et en celui du pays. 
Ils l'envisagent avec un optimisme nouveau. 
Le defi de l'avenir 
Les Canadiens ont etabli ces priorites pour cette nouvelle legislature. 
Ces priorites sont celles du gouvernement.  
Pour un Canada plus fort 
L'engagement le plus important du gouvernement est de maintenir l'unite du Canada. 
Il n'a pas de plus grand devoir ou responsabilite. 
Il faudra de la collaboration et des partenariats. 
Il faudra se tendre la main. 
Le Canada nous offre un espace et des moyens communs pour realiser notre potentiel. 
Et un pays qui investit dans ses enfants aura un avenir meilleur. 
De concert, nous edifions maintenant un regime de prestation nationale pour enfants complet et efficace.  
Nous avons les moyens d'ameliorer la vie de nos enfants. 
Investir dans le domaine de la sante et des soins de qualite 
Il assumera ce role dans un esprit d'ouverture, de pragmatisme et d'innovation. 
Cela veut dire repondre aux defis nouveaux. 
En collaboration avec ses partenaires, le gouvernement prendra les mesures suivantes: 
Les Canadiens reconnaissent que leur sante depend de beaucoup plus que des soins medicaux. 
La situation sociale et economique contribue egalement a determiner la qualite de la sante. 
Certains des problemes de sante les plus pressants se trouvent actuellement dans les collectivites autochtones. 
Le gouvernement travaillera de concert avec d'autres partenaires et avec les collectivites autochtones: 
Offrir des possibilites aux jeunes Canadiens 
La generation actuelle de jeunes Canadiens est la plus instruite de notre histoire. 
Aucun secteur de la societe, aucun gouvernement ne possede toutes les solutions. 
Les programmes de stage, en particulier, ont donne d'excellents resultats dans ce domaine. 
Investir dans le savoir et la creativite 
L'appui au savoir va au-dela de l'aide a la recherche universitaire. 
Accroitre les possibilites offertes aux communautes autochtones 
renforcer la capacite des communautes autochtones de bien se gouverner.  
Nous tirons grand plaisir de notre magnifique environnement, mais nous nous inquietons de son avenir. 
Le gouvernement fera cet effort.  
Il continuera de traiter le probleme international serieux de la surpeche. 
Dans le meme esprit d'internationalisme propre au Canada, le gouvernement prendra les mesures suivantes: 
Il poursuivra son travail de reforme des Forces armees canadiennes.  
Il poursuivra sa campagne de liberalisation du commerce. 
De nombreux Canadiens ont des idees et des suggestions originales de projets du millenaire. 
Sur la voie du XXIe siecle 
En travaillant tous ensemble, nous y parviendrons.  
Mesdames et Messieurs les deputes, 
Honorables senateurs et senatrices, Mesdames et Messieurs les deputes, 
Puisse la divine Providence vous guider dans vos deliberations.  
