La legalisation de la culture du chanvre industriel 
L'honorable Lorna Milne , ayant donne avis le 17 mars 1998: 
Nous aurons une nouvelle recolte cette annee, au Canada. 
Pour la premiere fois en 60 ans, la culture du chanvre sera legale. 
Non seulement il les a comprises, mais il a agi.  
Comme je l'ai declare au Senat le 19 juin 1996: 
Deux groupes ont comparu devant le comite pour discuter du chanvre. 
Le Canadian Industrial Hemp Lobby a aussi temoigne. 
Ils ont dit qu'une lourde charge de travail surtout avait ralenti leurs travaux. 
Honorables senateurs, ils ont respecte le delai.  
Le chanvre peut etre cultive partout au Canada. 
Cela signifie seulement trois parties par mille. 
Ceux qui tenteront l'experience n'obtiendront qu'un gros mal de tete.  
Les agriculteurs ont constamment l'impression de vivre sous la menace de la societe urbaine. 
Nous oublions souvent que les denrees alimentaires sont essentielles a la vie. 
Tout le Canada en beneficiera dans le cadre de l'economie mondiale. 
Il pourrait aussi y avoir d'importantes retombees pour l'industrie canadienne. 
De telles occasions se presentent parfois. 
J'invite les nouveaux senateurs a etre a l'affut de celles-ci. 
Attendez de voir cette nouvelle recolte au mois d'aout. 
Attendez de voir une premiere historique. 
Attendez de voir nos agriculteurs canadiens l'emporter sur leurs collegues du monde entier.  
