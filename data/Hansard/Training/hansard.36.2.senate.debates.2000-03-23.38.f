Son taux de chomage demeure trop eleve. 
Sa devise continue de se deprecier. 
Sa part des investissements etrangers directs est en baisse. 
Le revenu apres-impot reel des Canadiens stagne. 
Comment caracterise-t-il le Canada? 
Il dit que nous sommes underemployed, overtaxed and underproductive. 
On est loin des vantardises du ministre.  
Mais, venons-en au discours du budget du 28 fevrier dernier.  
Je voudrais d'abord feliciter le ministre pour la pleine indexation des parametres fiscaux. 
Il faut faire comme l'Irlande, dans l'Union europeenne. 
Rappelez-vous les debats sur la TPS et sur le libre-echange. 
Alors pourquoi le croirions-nous aujourd'hui?  
Et on appelle ca un bon politicien, dans certains milieux. 
Il veut intervenir un peu partout sauf la ou cela compte.  
Passons maintenant a l'impot sur le revenu des entreprises. 
En fait, depuis les annees 60, les impots sur les profits ont toujours augmente. 
La reponse, c'est que les impots sur le revenu des societes sont trop eleves. 
C'est la meme raison qui explique la diminution des investissements etrangers directs.  
C'est ici aussi que le ministre a ete beaucoup trop timide. 
Le president de Nortel a ete clair la-dessus.  
Je voudrais maintenant attirer votre attention, honorables senateurs, sur le plan des depenses publiques. 
Est-ce la aussi une nouvelle tendance? 
J'ajoute que plus la reglementation est omnipresente, plus la liberte d'entreprendre est compromise.  
Comme le soulignait M. R. Martin dans le Globe and Mail du 9 fevrier dernier: 
Les gouvernements ne peuvent pas choisir leurs gagnants, mais les perdants peuvent choisir leurs gouvernements.  
A peine 10 p. 100 du budget profite aux pauvres. 
Quand la prosperite est la, on peut se payer des politiques sociales genereuses. 
Autrement, c'est un effort additionnel d'egalite dans la pauvrete.  
Que peut-on demander de plus concluant? 
J'invite le leader du gouvernement a lire quelques-unes de ces recherches serieuses. 
Les budgets de depenses publiques expliqueraient 42 p. 100 des variations de croissance, selon eux. 
Je ne dis pas que ce facteur est le seul responsable de cette avance. 
Or, quel est le constat des auteurs cites plus haut? 
Or, nous sommes a bien au-dela de 40 p. 100 au Canada. 
Comment le gouvernement peut-il soutenir que les depenses gouvernementales sont desormais incompressibles?  
Il s'agit des transferts aux provinces. 
Ca, c'est la theorie, la justification de ce type de redistribution. 
Finalement, l'imputabilite des gouvernements est sacrifiee dans ce processus. 
Un dernier secteur de depenses qui est toujours inquietant, c'est celui de la sante. 
De 1965 a 1999, la sante a plus que double sa ponction du PIB. 
A ce rythme, nous sommes et resterons les plus taxes des pays du G-7. 
En terminant, je resume mes reflexions en quatre points. 
D'abord, le discours economique du gouvernement n'est pas convaincant. 
