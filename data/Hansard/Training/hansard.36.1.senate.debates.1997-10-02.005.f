Projet de loi modificatif-Deuxieme lecture-Ajournement du debat 
Nous vivons a une epoque caracterisee par une evolution demographique importante. 
Le nombre de Canadiennes et de Canadiens ages s'accroit rapidement.  
Il ne fait pas de doute que la loi doit etre modifiee. 
Dans certains cas, les cotisations requises pour financer ces ameliorations n'ont pas ete versees.  
Parmi ces principes, mentionnons les suivants: 
Il convient d'intervenir rapidement pour regler les problemes des regimes de retraite en difficulte; 
La reglementation et la surveillance doivent etre efficientes. 
Le regime de surveillance doit prevoir une reddition de comptes et une transparence adequates.  
Les employeurs, et, dans bien des cas, les employes cotisent a ces regimes. 
Cela ne signifie pas necessairement que les regimes doivent prendre fin. 
Cela m'amene a un autre point etroitement relie. 
Cela incite les organismes de surveillance a intervenir rapidement.  
Par ailleurs, le role des organismes de surveillance doit etre clairement defini. 
J'aimerais maintenant aborder un autre aspect de ce projet de loi. 
Les institutions financieres seraient egalement responsables de l'administration de ces regimes. 
Les dispositions concernant les regimes de retraite simplifies seront presentees a une date ulterieure.  
Le bureau participe a ce projet depuis environ deux ans. 
Par exemple, le pouvoir de surveillance du Bureau du surintendant des institutions financieres. 
Six nouveaux pouvoirs lui sont donc accordes. 
Ce pouvoir facilitera l'obtention des rapports exiges.  
Un autre pouvoir est celui de l'arbitrage en cas d'excedent de l'actif. 
Nous avons recu de nombreux commentaires, mais tres peu de suggestions concretes. 
En conclusion, honorables senateurs, ces mesures sont le fruit d'une vaste consultation. 
Le gouvernement espere d'ailleurs recevoir d'autres commentaires sur les reglements et lignes directrices.  
(Sur la motion du senateur Kinsella, le debat est ajourne.)  
