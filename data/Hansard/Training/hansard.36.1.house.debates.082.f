M. John Harvard (secretaire parlementaire du ministre de l'Agriculture et de l'Agroalimentaire, Lib.):
Cette industrie connait d'ailleurs une croissance impressionnante. 
Nos exportations continuent de s'accroitre, tout comme notre marche interieur.  
Nous ferons tout pour repondre a l'evolution des besoins de l'industrie. 
Ensemble, nous travaillons a l'elaboration de strategies en vue de parvenir a cet objectif.  
Je suis donc tres heureux de discuter de ce projet de loi aujourd'hui.  
L'industrie du grain divise les cultures en cultures ordinaires ou speciales. 
La premiere serie de consultations fut entreprise par un groupe de producteurs. 
Un consensus en la matiere s'est degage de ces discussions.  
En mars 1995, la commission a publie un rapport refletant ce consensus. 
Le gros de la production de cultures speciales vient des trois provinces des Prairies.  
En 1984-1985, la production etait d'environ un million de tonnes. 
En 1997-98, elle a grimpe a environ trois millions de tonnes. 
Les marche des cultures speciales continue de croitre. 
Les grands et les petits producteurs contribuent a cette remarquable croissance.  
Le secteur des cultures speciales exporte beaucoup. 
L'entree de nouveaux intervenants stimulera l'innovation et le developpement de nouveaux marches.  
