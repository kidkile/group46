Projet de loi modificatif-Deuxieme lecture-Ajournement du debat 
Honorables senateurs, je parlerai aujourd'hui du projet de loi C-247. 
Ce projet de loi me preoccupe pour trois raisons. 
Premierement, je crois qu'il est inconstitutionnel. 
Deuxiemement, je crois qu'il est regressif et contraire a la pratique penale reconnue. 
A premiere vue, je dois dire que cette proposition m'a semble interessante. 
C'est pourquoi nous perfectionnons la competence des procureurs de la Couronne. 
Les avocats chevronnes se chargent rarement de ces affaires. 
Honorables senateurs, ce projet de loi n'est pas une bonne mesure legislative. 
