LOI SUR LA CITOYENNETE AU CANADA 
Monsieur le President, je partage mon temps de parole avec le depute de Wentworth-Burlington. 
Qui dit citoyennete dit peuple et edification d'un pays. 
Son evolution represente l'essence meme de notre identite collective en tant que peuple.  
Pendant l'apres-guerre, le Canada a connu une croissance demographique sans precedent. 
A l'heure actuelle, plus de 90 p. 100 des demandes de citoyennete sont simples. 
Chaque demande doit quand meme etre approuvee par un juge de la citoyennete. 
Ce processus est long et couteux. 
