L'Afghanistan-La situation des femmes et des filles 
Depuis 1996, un groupe islamique radical detient le pouvoir dans ce pays. 
Les taliban menent une campagne systematique pour priver les femmes de leur identite. 
Elles ont perdu leur liberte, leur dignite et leur bien-etre physique.  
Les femmes sont marginalisees. 
Les ecoles de filles sont fermees. 
La loi ne protege pas les Afghanes. 
Les gardiens de l'ordre taliban, qui sont jeunes et souvent illettres, font la loi. 
Les hommes ont droit de vie et de mort sur les femmes de leur famille. 
Pour certaines, le suicide est la seule issue possible. 
Meme parmi les fondamentalistes, les taliban sont des extremistes. 
Auparavant, les Afghanes jouissaient d'une liberte relative. 
On trouvait des femmes aux echelons superieurs de la fonction publique afghane.  
Nous devons donc trouver d'autres facons d'exprimer nos protestations. 
Nous devons continuer de protester contre cette situation consternante. 
L'honorable Wilbert J. Keon :
