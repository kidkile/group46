Le gouvernement a pris des mesures pour promouvoir le benevolat dans notre pays. 
Les demographes nous montrent que la face de la planete est en train de changer. 
Tres souvent, cette contribution prend la forme de benevolat au sein de nos collectivites.  
Au cours de cette annee-la, j'ai travaille avec beaucoup de benevoles. 
La contribution des personnes agees a ete inestimable. 
On ne peut pas estimer la valeur de ces experiences tres humaines.  
Ce ne sont la que quelques exemple. 
Il y en a de plus simples, qui sont tout aussi importants. 
