Au Canada, le benevolat a toujours ete present et innovateur. 
Trop souvent, nous tenons cette contribution pour acquise. 
Nous sommes-nous demande combien ces benevoles font economiser a notre systeme de sante? 
Les foyers de nombreuses localites canadiennes sont proteges par des pompiers volontaires. 
Pouvons-nous quantifier ces economies? 
Sur le plan monetaire, leur contribution est incommensurable.  
Le benevolat favorise la comprehension. 
Ce sont ces evenements qui font du Canada une grande famille.  
