Honorables senateurs et senatrices, 
Mesdames et Messieurs les deputes, 
La technologie modifie tous les aspects de notre vie. 
Le savoir et la creativite constituent desormais le moteur de la nouvelle economie. 
Cette vision et cette volonte ont ete largement recompensees.  
Ils ont en outre cree un regime de securite sociale moderne. 
Toutes ces realisations constituent les assises de notre qualite de vie.  
Aujourd'hui, les Canadiens et les Canadiennes peuvent s'enorgueillir du succes de leur pays. 
Nous sommes reconnus dans le monde entier pour notre qualite de vie. 
UN CANADA FORT ET UNI 
Il garantit les droits et les libertes de tous les citoyens. 
Nos artistes, nos ecrivains, nos musiciens et nos cineastes font l'admiration du monde entier. 
Ensemble, nous serons en quete de l'excellence. 
Nous devrons collaborer avec nos partenaires dans les domaines suivants afin: 
- de creer une economie dynamique; 
- d'ameliorer la sante et la qualite des soins pour les Canadiens; 
- d'assurer la qualite de notre environnement; 
- de renforcer les relations avec les peuples autochtones du Canada; et 
- d'elargir le role du Canada dans le monde.  
NOS LEADERS AU XXIE SIECLE 
C'est des la tendre enfance que de telles qualites s'acquierent. 
Ce plan respectera l'Entente-cadre sur l'union sociale. 
Nos jeunes sont les leaders de demain. 
Ils sont deja parfaitement a l'aise dans le monde branche. 
UNE ECONOMIE DYNAMIQUE POUR LE XXIE SIECLE 
Les Canadiens ont bati une economie forte et dynamique. 
Le gouvernement est resolu a poursuivre une politique de gestion financiere prudente. 
Il fera en sorte que la dette diminue constamment par rapport au produit interieur brut. 
Augmentation des echanges commerciaux et des investissements 
Les marches etrangers offrent de nouveaux debouches pour nos biens et nos services. 
Competences et savoir necessaires au XXIe siecle 
Il a facilite l'epargne-etudes pour les enfants. 
Nous continuerons a nous appuyer sur cette strategie. 
Au cours des deux prochaines annees, le gouvernement travaillera avec ses partenaires afin de: 
- permettre aux Canadiens de financer plus aisement l'apprentissage continu; et 
L'infrastructure du XXIe siecle 
Pour realiser cet objectif, il faudra creer de nouveaux types d'infrastructure.  
Pour ce faire, il: 
Le gouvernement deviendra un utilisateur modele des technologies de l'information et d'Internet. 
L'economie du savoir n'est pas le simple produit des entreprises de haute technologie. 
La force du Canada trouve racine dans sa riche diversite. 
Le Canada devra en outre continuer a ameliorer son infrastructure materielle au XXIe siecle. 
LA SANTE ET LES SOINS DE QUALITE POUR LES CANADIENS 
LA QUALITE DE NOTRE ENVIRONNEMENT 
Le gouvernement continuera aussi d'elargir le reseau de parcs nationaux du Canada.  
Dans ses propres activites, le gouvernement se posera comme modele d'excellence environnementale. 
Il agira davantage pour depolluer les sites federaux contamines. 
Il renforcera sa capacite scientifique de recherche dans le secteur de l'environnement. 
Il explorera egalement de nouvelles technologies de depollution.  
Le gouvernement collaborera avec les Canadiens pour que nos communautes demeurent sures. 
Il modernisera en outre les lois pour mieux assurer la securite publique.  
