L'honorable Eugene Whelan , ayant donne avis le 16 fevrier 1999: 
De nombreux etablissements eriges le long du chemin de fer clandestin sont toujours en exploitation. 
C'est le cas de l'eglise episcopale methodiste africaine Nazrey a Amherstburg, en Ontario. 
Erigee en 1848, cette eglise est issue d'une congregation methodiste anterieure etablie en 1826. 
Elle a ete designee monument national historique en decembre 1998. 
Depuis dix ans, on recueille des fonds regulierement, mais lentement. 
Dans l'intervalle, la stabilite de l'eglise s'est graduellement deterioree. 
On a cru que l'eglise pourrait s'effondrer. 
On dit que les voies de Dieu sont impenetrables. 
Ils ont ensuite rencontre les membres du conseil d'Amherstburg. 
C'est avec plaisir que je vous annonce que ce miracle a eu lieu. 
En depit de ces apports, il manque encore des fonds. 
