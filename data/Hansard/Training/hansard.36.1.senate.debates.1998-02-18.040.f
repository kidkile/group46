La Loi sur la reorganisation et l'alienation de teleglobe canada 
Projet de loi modificatif-Deuxieme lecture-Suite du debat 
L'honorable William M. Kelly :
Notre groupe a ete charge de deux taches. 
Comme vous le savez, Teleglobe a ete privatisee en 1987. 
Les tarifs ont chute, et les services se sont multiplies. 
Cela a ete extremement avantageux pour les entreprises et les consommateurs. 
Les resultats de cet examen n'ont pas ete divulgues. 
Parlons maintenant de Teleglobe. 
Je reviendrai a cette decision dans un instant.  
Teleglobe a elle-meme recours a ce systeme. 
A mon avis, elles devraient d'abord apprendre a soutenir la concurrence au pays. 
Encore une fois, il serait bon de revenir un peu en arriere. 
C'est dans cette optique que l'on doit examiner le projet deloi C-17. 
Est-ce que cette mesure va stimuler la liberalisation, la concurrence et le choix? 
J'espere que nous aurons quelques reponses.  
L'honorable Noel A. Kinsella (chef adjoint suppleant de l'opposition) :
Que signifie hubbing ou concentration en l'occurrence?  
Je suppose que notre propre gouvernement a commis la meme erreur. 
Oui, cela devrait depasser la proposition.  
(Sur la motion du senateur Kinsella, au nom du senateur Oliver, le debat est ajourne.)  
