Les preoccupations des Albertains-Interpellation-Ajournement du debat 
L'honorable Ron Ghitter , ayant donne avis lemardi 10 fevrier 1998: 
Nos politiciens sont les cibles preferees. 
Cela constitue un probleme grave, un probleme qui exige reflexion, discussion et action.  
Serait-il bon, dans l'interet superieur du Canada, d'abolir le Senat? 
En tant qu'Albertain, je repondrai clairement par la negative. 
En quoi donc avons-nous fait fausse route?  
Je ne partage cependant pas leur optimisme. 
Il y a quelque chose d'attristant dans ce que je viens de dire. 
Honorables senateurs, le moment est venu, je crois, de regarder la verite en face. 
Le moment est venu pour nous d'etre des agents de changement. 
J'attends vos commentaires. 
Depuis les quelques annees que je suis ici, ce sujet n'a jamais ete debattu. 
Le moment est venu de le faire. 
Ce sujet est trop important pour etre neglige. 
L'initiative doit venir de l'interieur. 
Si nous representons vraiment nos regions, pourquoi representons-nous des partis politiques? 
Que faisons-nous ici, a penser comme des politiciens? 
Nous ne sommes pas censes etre des politiciens. 
Je suppose que c'est vrai egalement. 
Les groupements politiques utilisent souvent le Senat pour se faire du capital politique. 
Que pouvons-nous faire? 
Deuxiemement, je recommanderais que les provinces ou les cinq regions du Canada soient representees egalement. 
Le statu quo tire a sa fin. 
Les Canadiens ne tolereront pas la situation actuelle encore longtemps. 
Les signes sont la. 
Autrement dit, il faut mener, sinon on se fait mener.  
Je suis tout a fait d'accord avec ce point de vue.  
Honorables senateurs, je compte sur vous pour jouer un role actif suite a cette interpellation.  
