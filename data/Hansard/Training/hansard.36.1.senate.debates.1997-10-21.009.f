La Loi canadienne sur les droits de la personne 
Projet de loi modificatif-Deuxieme lecture-Ajournement du debat 
Voila qui est fait.  
Le dernier discours du Trone promet que: 
Permettez-moi un bref historique.  
Il est entre en vigueur le 30 juin 1992.  
Nous reduisons l'ecart entre le geste et la parole. 
J'aborderai d'abord les modifications proposees au droit penal.  
Le principe de l'obligation de satisfaire les besoins n'est pas nouveau. 
La mesure proposee ici s'inspire de la jurisprudence de la Cour supreme du Canada. 
Le gouvernement estime que c'est un equilibre raisonnable. 
Ce sont des valeurs cheres aux Canadiens et que le gouvernement est determine a maintenir. 
Honorables senateurs, j'espere que vous appuyerez ce projet de loi.  
L'honorable Noel A. Kinsella (chef adjoint suppleant de l'opposition) :
Cette disposition repose sur un principe que je conteste. 
Honorables senateurs, cette disposition modifie la composition du Tribunal canadien des droits de la personne. 
C'est bien, tres bien meme, a mon sens. 
Honorables senateurs, ce projet de loi a un preambule. 
Je trouve que c'est un excellent principe. 
Un traitement identique ne signifie pas toujours l'egalite. 
Ce dont nous parlons ici, c'est d'action positive. 
C'est ce que l'on postule dans le troisieme paragraphe du preambule.  
Il faut examiner le principe de la discrimination. 
Voila donc un autre article bienvenu que j'appuie en principe.  
L'article 16.1 du projet de loi stipule que: 
Il serait donc tres utile d'avoir en notre possession une copie de la documentation.  
Cela n'a rien d'un changement d'ordre administratif ou mineur. 
Il y a la bon nombre d'erreurs. 
There is hereby established a tribunal to be know as the Canadian Human Rights Tribunal...  
Alors que la version francaise dit plutot ceci: 
Je ne sais pas sur quel pied danser a ce sujet.  
(Sur la motion du senateur Kinsella, le debat est ajourne.)  
Projet de loi modificatif-Deuxieme lecture 
L'honorable J. Michael Forrestall :
Le processus legislatif devrait s'en trouver a tout le moins ameliore.  
M. Johnson a egalement explique le rapport qui existe entre le bureau et Transports Canada. 
Il a dit que Transports Canada est l'executeur. 
Le bureau doit convaincre Transports Canada d'appliquer ses recommandations. 
Le temoin a ajoute: 
Honorables senateurs, cela m'amene a parler de la mesure a l'etude. 
Ce que nous y avons appris pourrait bien s'appliquer ici au Canada. 
Je crains toutefois que le bureau ne travaille qu'a temps partiel. 
Le projet de loi fait naitre cette crainte. 
Le paragraphe 4(1) de la meme loi est remplace par ce qui suit: 
Permettez-moi de lire le texte que ce paragraphe remplace. 
A l'heure actuelle, le libelle est le suivant: 
Nous sommes passes de cinq membres a temps plein a cinq membres. 
Voila d'ou vient notre inquietude.  
C'est l'occasion revee d'apporter des modifications a cette loi qui la renforcent.  
Il s'agit de preoccupations auxquelles nous devons nous consacrer a temps plein. 
Il faut donc renforcer ce mecanisme au maximum. 
Le president Clinton a propose de debourser 90 millions de dollars pour contrer cette tendance.  
Ce projet de loi revet une importance vitale. 
Nous nous devons de l'etudier de pres. 
Nous ferons donc surement de notre mieux.  
