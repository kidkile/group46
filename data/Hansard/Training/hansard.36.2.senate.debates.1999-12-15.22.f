Projet de loi de 1999 pour la mise en oeuvre de conventions fiscales 
Troisieme lecture-Suite du debat 
L'honorable A. Raynell Andreychuk :
Honorables senateurs, le projet de loi S-3 a ete renvoye a deux comites. 
Le Parlement en recoit depuis fort longtemps. 
Ils doivent reconnaitre la primaute du droit.  
Honorables senateurs, on a beaucoup insiste sur la question de la confidentialite. 
Heureusement, le nombre de ces pays diminue, mais ils sont encore un sujet de preoccupation.  
Cette observation a ete faite dans cette enceinte. 
Je pense que nous devrions tenir compte de ces propos.  
Dans cette enceinte, nous sommes tres fiers de nos rapports. 
Il faut les considerer simultanement avec les droits de la personne.  
Il ne faut pas separer les droits de la personne des autres questions. 
Les droits de la personne impregnent tous nos plans d'action et toutes les lois. 
Nous devons examiner tous les enjeux.  
Cependant, un des problemes, c'est que nous n'avons plus de ministere du Revenu. 
Les employes du ministere du Revenu n'ont pas les memes responsabilites a assumer. 
L'Agence du revenu met en oeuvre une politique fiscale. 
Elle est dotee d'un mandat strict et elle l'observe. 
Cela ne fait pas partie de son mandat, pour l'instant du moins.  
Le senateur Lynch-Staunton a cite l'exemple de l'Ouzbekistan. 
Le bilan de l'Ouzbekistan n'est pas exemplaire. 
L'Ouzbekistan est encore hypotheque par les vestiges de l'Union sovietique. 
L'Union sovietique lui a laisse un heritage desastreux sur les plans ecologique et politique. 
Personne ne semble avoir analyse le probleme.  
Nous ne prenons aucun pays particulier a titre d'exemple. 
Il serait donc injuste de pointer un pays du doigt a ce stade. 
Je ne pense pas que nous devrions juger les gens et les pays au hasard. 
Il faut juger de maniere coherente et systematique.  
Le projet de loi S-3 montre que nous n'avons pas reussi le test. 
Nous devons mettre de l'ordre chez nous.  
