L'hon. David M. Collenette (ministre des Transports, Lib.) propose: 
Des transports rapides, fiables et economiques sont indispensables a l'economie canadienne. 
De nombreuses recommandations destinees a ameliorer le systeme maritime sont ressorties de ce rapport.  
On n'y a absolument rien change. 
Si on doit y apporter des modifications, le comite envisagera cette possibilite.  
Nous considerons qu'il s'agit d'une mesure importante. 
C'est pourquoi nous l'avons presentee sous sa forme initiale. 
Elle est essentielle a la competitivite de notre secteur maritime.  
Dans l'ensemble, elle ameliorera la competitivite de notre secteur maritime.  
Le projet de loi repond a sept grands objectifs. 
Quatriemement, il fournit un niveau eleve de securite et de protection de l'environnement. 
Sixiemement, il assure une gestion de l'infrastructure maritime conforme aux bonnes pratiques commerciales. 
Septiemement, il prevoit le transfert de certains ports et installations portuaires.  
Quatre grands elements composent ce projet de loi. 
Il traite aussi de la voie maritime et des ports eux-memes.  
Il faut moderniser le reseau portuaire. 
Cela ne fait aucun doute.  
Pour qu'on accede a sa demande, un port doit respecter quatre criteres. 
Elles ne recevront pas de financement du gouvernement federal. 
L'ere des subventions federales est revolue.  
Les APC auront le statut de mandataire de la Couronne. 
Le statut de mandataire se limitera aux activites portuaires essentielles.  
Les APC devront trouver leur financement dans le secteur prive. 
Le gouvernement federal ne sera plus la pour eponger leurs dettes.  
Le processus de cession de ces ports est deja bien en marche. 
Le gouvernement du Canada continuera de garantir le fonctionnement des ports dans les regions eloignees. 
Cette politique sera appliquee de facon equitable et uniforme partout au Canada.  
C'est d'ailleurs la premiere region maritime du Canada. 
Au Cabinet, mes collegues et moi seront sensibles a ces preoccupations. 
