L'impact des politiques federales sur les populations cotieres-Interpellation-Fin du debat 
L'honorable Pat Carney , ayant donne avis le 30 avril 1998: 
Nous voulons une solution adaptee aux problemes de la cote Ouest. 
Nous avons encore du poisson. 
La peche du saumon du Pacifique est une industrie de pointe. 
Pour fonctionner, ce programme doit viser et corriger trois problemes fondamentaux.  
Cela a oblige les pecheurs a acheter des droits de peche supplementaires. 
Nous ne parlons pas ici de fonds provisoires. 
Nous parlons ici de travail reel pour des gens reels.  
Cela montre que les peches doivent etre administrees dans la region.  
Personnellement, je preconise depuis longtemps une gestion conjointe des peches. 
Cela pourrait aussi se faire en Colombie-Britannique pour le secteur des peches.  
Le cas des quais de ravitaillement en combustibles illustre bien le dilemme des localites cotieres. 
Quand nous perdrons ces derniers, nous perdrons aussi le quai de ravitaillement communautaire. 
La diminution des stocks de poisson aura un impact enorme sur toute la cote. 
Toute offre d'aide federale doit en tenir compte. 
M. Fortier estime que notre position est ...valable, justifiee, raisonnable et realiste. 
Il y a plus que notre juste part qui est en jeu. 
Son Honneur le President suppleant :
Si aucun autre senateur ne veut intervenir, le debat est termine.  
