Le financement de l'enseignement postsecondaire 
L'honorable Lois M. Wilson :
En ce moment meme, le manque de financement se fait sentir dans les classes. 
Voila qui cree un probleme d'accessibilite pour les etudiants pauvres qui se trouvent desavantages.  
Prenons le cas de l'Universite Lakehead a Thunder Bay. 
Cela comprend 5 308 etudiants a temps plein des deuxieme et troisieme cycles. 
L'Universite Lakehead represente un puissant moteur economique pour cette region de l'Ontario. 
Elle s'enorgueillit d'un programme de foresterie unique en son genre au Canada. 
Les zones ont ete modifiees pour la derniere fois il y a dix ans. 
Jusqu'ici, cela n'a pas ete fait. 
Par consequent, l'Universite Lakehead se trouve dans une situation intenable. 
Elle doit demander aux gouvernements de creer des regles du jeu equitables.  
La crise n'est pas pres de se dissiper. 
En fait, elle va s'aggraver. 
C'est maintenant qu'il faut agir.  
(Sur la motion du senateur Hays, le debat est ajourne.)  
