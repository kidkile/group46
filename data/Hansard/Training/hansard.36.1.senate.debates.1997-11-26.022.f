Projet de loi de mise en oeuvre de la convention sur les mines antipersonnel 
Je vous pose la question et j'entends votre reponse.  
Mais les morts, vous ne les voyez plus. 
Mais vous ne voyez plus les milliers de morts. 
Il faut revenir a la raison. 
Il faut que la technique meurtriere soit soumise a l'esprit humanitaire. 
Que cessent la tuerie et la mutilation de populations innocentes.  
Il y a de l'espoir; un jour nouveau pointe a l'horizon. 
On s'attend a ce que plus de 100 Etats signent ce traite.  
Nous savons maintenant que nous releverons ce defi. 
Des Etats ne signeront pas la convention a Ottawa la semaine prochaine. 
Aucune convention n'emporte l'adhesion universelle lors de son ouverture a la signature. 
Au yeux de l'opinion internationale, le Canada est intimement lie a cette convention. 
Et cela ne sera pas termine pour autant. 
Pour y arriver, le gouvernement propose ce projet de loi. 
Le projet de loi C-22 vise plusieurs objectifs. 
En effet, la convention autorise l'emploi des mines a ces fins.  
Et il est impossible de savoir combien de mines antipersonnel supplementaires sont fabriquees chaque annee. 
Ces donnees depassent l'entendement! 
La situation dans bien des pays est deja critique. 
Nous devons mettre fin a ce cycle abominable.  
Le travail n'est pas termine! 
Quarante est le nombre a retenir. 
Quarante pays doivent ratifier la convention pour qu'elle devienne loi internationale. 
Honorables senateurs, j'ai assez parle. 
Ces accords etaient rarement destines a reduire la brutalite dans le monde. 
Honorables senateurs, la convention est une victoire marquante pour les droits de la personne. 
Il est significatif et approprie que la ceremonie de signature ait lieu a Ottawa. 
Nous savons tous que ce n'est pas accidentel. 
Comme on a pu le lire dans The Economist : 
Cette approche, reaffirmee a Oslo, sera bientot adoptee par plus de 100 pays. 
Tous les pays signataires devront, sans exception, cesser toute activite liee aux mines antipersonnel. 
Cette convention est simple et claire. 
Toutefois, on ferait bien de se rappeler que le Canada n'est pas sans tache. 
Des Canadiens aussi sont sautes sur des mines terrestres. 
Le processus de mise en oeuvre sera egalement difficile. 
(La motion est adoptee, et le projet de loi est lu une deuxieme fois.)  
