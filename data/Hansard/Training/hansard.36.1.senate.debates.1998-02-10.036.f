Les Etats-Unis, l'Australie, la Grande-Bretagne et le Japon ne le font pas. 
De nombreuses affirmations et denegations ont ete formulees des deux cotes de la Chambre. 
Je fais plutot appel au bon sens, notamment au bon sens politique. 
Nous nous devons a nous-memes de donner suite a cette declaration.  
Je ne parle pas ici d'un consensus entre les parties interessees seulement. 
Cependant, ces groupes seuls ne forment pas un consensus national. 
Pour cela, on a besoin de gens, de beaucoup de gens. 
Je pense qu'un tel groupe existe. 
Il s'agit de Canadiens moyens et non d'interets speciaux. 
Il me semble improbable que ces chiffres aient change radicalement depuis les dernieres elections.  
Je serais tres surpris d'apprendre qu'il ne pense plus ainsi.  
Je suis sur que la plupart d'entre vous les connaissez. 
Taxer l'investissement n'a jamais ete une bonne idee. 
C'est aussi taxer les emplois d'une certaine maniere. 
En outre, 34 p. 100 ne peuvent lire que des choses simples. 
Je suis sur que vous conviendrez avec moi que ces chiffres sont alarmants. 
En termes larges, disons que l'alphabetisation touche notre productivite. 
Elle a des consequences sur notre competitivite. 
Elle influence notre aptitude a faire des affaires dans une economie globale. 
Il est donc tres important que nous nous assurions que tous nos concitoyens savent lire.  
Nous abordons ici le vif du sujet. 
C'est le bons sens qui parle. 
Cela s'applique tout particulierement aux jeunes. 
Les jeunes representent notre avenir. 
C'est notre bien le plus precieux. 
De nos jours, l'economie est fondee sur les services et la technologie. 
Elle permettent de fournir les nombreux services dont nous avons besoin dans notre societe. 
Toutefois, le materiel de lecture devrait faire exception a la regle generale.  
L'elimination de la TPS sur le materiel de lecture devrait etre une priorite. 
C'est logique sur le plan economique. 
