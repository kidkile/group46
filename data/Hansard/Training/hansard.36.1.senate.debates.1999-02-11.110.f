La situation de la pauvrete au Canada 
L'honorable Therere Lavoie-Roux , ayant donne avis le mardi 9 fevrier 1999: 
La pauvrete a de multiples visages. 
Mais qu'est-ce donc que la pauvrete exactement? 
Comment mesure-t-on la pauvrete? 
Les familles a faible revenu sont celles qui sont en plus mauvaise posture au Canada. 
La pauvrete est un probleme tres repandu au Canada. 
Quelque 17,9 p. 100 des Canadiens sont pauvres. 
Un sur cinq enfants vit dans la pauvrete au Canada.  
Comment pouvons-nous, honorables senateurs, tolerer une telle chose? 
Ce n'est pas acceptable.  
Ces chiffres attestent que certains enfants sont grandement desavantages par rapport a d'autres.  
Nous perdons du terrain au lieu d'en gagner. 
J'ai parle de la pauvrete infantile et des familles a faible revenu. 
Nous savons egalement que beaucoup de femmes vivent dans la pauvrete. 
Les personnes agees aussi sont exposees a la pauvrete. 
Nous avons lu des articles sur ce sujet pendant le conge des fetes. 
Les statistiques sont alarmantes. 
Les personnes handicapees aussi ont des revenus inferieurs a ceux de la population en general. 
La plupart des Canadiens handicapes sont pauvres. 
