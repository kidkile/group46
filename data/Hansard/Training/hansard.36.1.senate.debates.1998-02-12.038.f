Feu l'honorable juge Mark McGuigan, c.p., c.r. 
Honorables senateurs, l'honorable juge Mark McGuigan nous a quittes, il y a quelques semaines.  
Il naquit a l'Ile-du-Prince-Edouard en 1931. 
Il a fait ses etudes de droit a Osgoode Hall et a l'Universite Colombia. 
Professeur de droit, doyen, il a excelle dans sa carriere universitaire.  
Apres une carriere universitaire, suivie d'une carriere politique, il a entrepris une carriere judiciaire. 
En 1984, on l'a nomme a la Cour d'appel federale. 
C'etait un juge eminent et on se rappellera de ses decisions.  
