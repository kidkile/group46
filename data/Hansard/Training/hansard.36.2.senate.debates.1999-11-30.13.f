Quelle est notre vision? 
Soixante pays s'appauvrissent regulierement depuis 1980. 
Les gouvernements reduisent leurs deficits au detriment des pauvres.  
Dans un monde d'abondance, la justice sociale semble plus eloignee que jamais. 
Nous participons a des guerres qui ne devraient pas avoir lieu. 
Les grandes puissances possedent des armes nucleaires qui mettent constamment en danger l'humanite. 
Les gouvernements de la planete financent un militarisme excessif au detriment des pauvres.  
C'est la la lecon que nous devrions tirer desannees 90.  
L'arrogance excessive de l'OTAN renforce maintenant l'inegalite et la mefiance. 
Les Russes et les Chinois n'accepteront jamais un univers domine par l'OTAN.  
Deja, les consequences de la guerre du Kosovo ont depasse de loin le cout humain. 
Le danger que presentent les armes nucleaires va grandissant. 
Comment le droit international sera-t-il impose dans les annees a venir? 
Voila la question fondamentale qui se pose au Canada a l'aube du nouveau millenaire.  
Ce mouvement peut creer les conditions favorables a une culture de la paix. 
Cette analyse est superficielle. 
Les etres humains ne sont pas genetiquement programmes pour faire la guerre. 
Il n'y a aucun element biologique inherent a notre nature qui produit la violence. 
Pour chasser le pessimisme actuel, il faut reconnaitre que la guerre n'est pas inevitable. 
La guerre et la violence sont generalement la consequence de decisions politiques deliberees. 
C'est l'essence d'une approche pour une culture de la paix. 
On n'acquiert pas un esprit de paix du jour au lendemain. 
