La declaration du Vatican en tant que document pedagogique-Interpellation 
L'honorable Jerahmiel S. Grafstein , ayant donne avisle 25 mars 1998: 
On peut considerer ce document un peu comme un aveu de culpabilite. 
Le document dit d'abord ceci: 
Au XIXe siecle s'etablit un nationalisme faux et exacerbe.  
On peut lire plus loin: 
Une forme extremiste du nationalisme etait mise en avant en Allemagne.  
La Solution finale a ete percue comme le prolongement logique d'un programme nationaliste.  
Le pape Jean Paul II a prone la suprematie de l'individualite sur la collectivite. 
Dans son encyclique  Centesimus Annus , il ecrit: 
Il y a un certain du a l'homme parce qu'il est homme.  
De quoi faut-il se rappeler?  
Bernard de Clairvaux avait developpe cette attitude sainte a l'epoque sombre du Moyen-Age.  
Clement VII a egalement autorise les juifs a pratiquer des metiers et professions. 
Nous sommes tous des Semites en esprit.  
Son successeur a ete le pape Pie XII.  
Evidemment, Rome etait alors en securite, aux mains des forces alliees. 
... le devoir de tout individu d'obeir aux imperatifs de sa conscience.  
Desespere, le cardinal Tisserant disait: 
D'autres catholiques peuvent temoigner encore plus formellement du silence de la papaute.  
Honorables senateurs, les papes sont peut-etre infaillibles, mais ils ne sont pas parfaits. 
Quelle est la nature de l'ecart separant ces deux ecoles de pensee tres proches? 
