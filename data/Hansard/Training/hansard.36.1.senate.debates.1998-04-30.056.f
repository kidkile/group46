Projet de loi de 1997 modifiant l'impot sur le revenu 
Deuxieme lecture-Ajournement du debat 
Ce projet de loi couvre vraiment un tres large eventail de sujets. 
Comme le ministre des Finances l'a declare dans son premier expose budgetaire: 
Il s'agit pour le Canada d'un premier budget equilibre en 30 ans. 
Ce n'est pas tout.  
Pour notre gouvernement, il ne suffit pas d'avoir un budget equilibre. 
Tel est le contexte dans lequel je veux situer le projet deloi C-28. 
C'est le genre d'investissement que tous les Canadiens peuvent apprecier. 
Le projet de loi C-28 comporte un autre aspect important. 
Par consequent, elles ont ete etudiees de pres par des specialistes du secteur prive. 
Atteindre cet objectif national essentiel exigeait un effort national. 
La question consiste a determiner si le gouvernement a agi avec equite.  
La reponse est oui.  
Le gouvernement a donc restructure en un seul transfert l'assemblage de diverses composantes distinctes. 
Il a comprime ses propres depenses plus que celles des autres. 
Les chiffres le montrent. 
Je voudrais maintenant vous montrer comment cela s'est produit, deja. 
D'ou vient ce milliard de plus?  
Cela met en lumiere les avantages de l'element points d'impot du TCSPS. 
