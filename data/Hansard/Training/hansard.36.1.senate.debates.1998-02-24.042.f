Je partage son opinion. 
On demande aux Canadiens de participer davantage a la planification de leur retraite. 
Les medias ont aussi souvent commente la necessite de relever le seuil des avoirs etrangers. 
Ces problemes disparaitraient si la regle absurde des 80 p. 100 etait abolie.  
Indeniablement, honorables senateurs, le fonds du RPC doit assurer la retraite du Canadien moyen. 
Il convient de souligner trois choses.  
C'est ce que l'on pourrait appeler une position de nationaliste economique. 
Jusqu'a maintenant, nous n'avons vu aucune preuve a cet egard. 
Il me semble, d'apres l'experience du Canada, que ce serait plutot le contraire. 
