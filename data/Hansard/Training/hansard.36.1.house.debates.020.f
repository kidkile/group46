Nous savons tous que la prosperite du Canada repose sur le commerce. 
Cela suppose que les entreprises devront elles aussi etre pretes a temps. 
Parce que le Tarif des douanes actuel constitue un obstacle hautement inutile aux echanges commerciaux. 
Il est complexe et desuet. 
Notre regime est devenu vraiment tres complexe. 
L'Organisation mondiale du commerce en a fait etat dans de nombreux rapports. 
Il est imperatif de minimiser les couts pour l'industrie. 
Notre systeme tarifaire doit etre mieux adapte a l'economie industrielle concurrentielle du Canada. 
Nous souhaitons ainsi aider nos entreprises a etre plus competitives et prosperes.  
Les reductions tarifaires prevues seront a l'avantage des producteurs canadiens. 
Enfin, le Tarif des douanes deviendra d'une application plus previsible.  
En effet, ce nombre passera de 11 000 a 8 000. 
En outre, des reglements tarifaires, desuets ou superflus, et des procedures administratives, seront elimines.  
De meme, nous abolirons les taux qui ne sont que des nuisances. 
Les taux de moins de 2 p. 100 seront elimines. 
Nous arrondirons les autres taux au demi pour cent pres. 
Ces caracteristiques s'ajouteront en permanence a notre systeme tarifaire.  
Elles permettront aux fabricants canadiens d'abaisser leurs couts de production. 
En bref, nos industries seront mieux en mesure de soutenir la concurrence pour des emplois.  
La paperasserie gouvernementale nuit egalement a la competitivite. 
La encore, cela augmentera notre competitivite, particulierement dans le secteur manufacturier.  
C'est pourquoi nous proposons d'eliminer environ 1000 de ces codes. 
La encore, toutefois, ce programme ne se justifie plus guere vu la reduction des droits.  
Toute exoneration accordee fera l'objet de modifications a l'annexe.  
Quelque 70 autres reglements seront remplaces dans l'annexe par des dispositions simplifiees. 
De plus, le gouvernement propose l'elimination du tarif de preference britannique. 
Dans l'ensemble, nous aurons un regime beaucoup plus simple, transparent et previsible.  
Ensuite, notre regime de droits sera plus transparent, plus previsible et plus simple. 
