Le discours du Trone 
L'honorable Jean B. Forest , appuyee par l'honorable senateur Mercier, propose: 
QU'IL PLAISE A VOTRE EXCELLENCE: 
Le gouvernement lui-meme n'a pas ete epargne. 
Tout le monde a du se serrer la ceinture. 
Rares sont ceux qui ont survecu sans faire de sacrifices.  
Le deficit est en train de baisser et il est presque elimine. 
Dans l'ensemble, l'economie se porte bien. 
Le taux d'inflation demeure bas, a l'instar des taux d'interet. 
Comme le ministre des Finances l'a si bien dit: 
C'est un gouvernement capable d'exercer ses obligations.  
Je voudrais parler un peu de quelques-unes de ces priorites. 
La premiere priorite de ma liste, c'est batir un Canada plus fort. 
C'est la un projet auquel j'ai oeuvre toute ma vie. 
Il est bon que le gouvernement soit pret a investir dans ces enfants. 
Je me permets de vous le lire pour conclure mon intervention: 
Un vieil homme, cheminant le long d'une route, 
Arriva au soir d'une journee grise et froide 
Au bord d'un gouffre large et profond 
Au fond duquel coulait un noir courant.  
Le vieil homme le traversa au crepuscule - 
Les eaux sombres ne lui inspirant aucune crainte; 
Une fois parvenu de l'autre cote, il se retourna 
Et construisit un pont pour franchir le fosse.  
Vieil homme, lui dit un autre voyageur pres de lui, 
Vous gaspillez vos forces en construisant ici.  
Votre voyage se termine a la fin de la journee.  
Vous n'aurez plus jamais a passer par ici.  
Vous avez traverse le gouffre large et profond, 
Pourquoi construisez-vous un pont a la tombee du jour?  
Le vieux constructeur leva sa tete grise.  
Cher ami, sur la route que j'ai suivie, dit-il, 
J'ai vu aujourd'hui qui me suivait 
Un jeune homme dont les pas l'ameneront ici.  
Ce gouffre qui ne me posait aucun probleme 
Pourrait lui paraitre infranchissable.  
Il doit, lui aussi, traverser au crepuscule; 
Cher ami, c'est pour lui que je construis ce pont.  
Honorables senateurs, nous pouvons reussir si nous essayons.  
(Sur la motion du senateur Mercier, le debat est ajourne.)  
(Le Senat s'ajourne a 14 heures demain.)  
