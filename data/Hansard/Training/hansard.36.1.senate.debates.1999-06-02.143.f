Projet de loi sur l'office d'investissement des regimes de pensions du secteur public 
Deuxieme lecture-Ajournement du debat 
De toute evidence, l'inaction n'est pas une option envisageable.  
Malheureusement, il a ete impossible d'aboutir a un accord final. 
Voici la facon dont le projet de loi aborde les surplus. 
Premierement, il prevoit une reduction graduelle des surplus actuels sur une periode de 15 ans. 
Toute augmentation sera limitee a 0,4 p. 100 des gains annuels.  
Elles seront benefiques pour tous les interesses. 
Le temps d'agir est venu depuis longtemps. 
(Sur la motion du senateur Stratton, le debat est ajourne.)  
