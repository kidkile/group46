Projet de loi de 1998 modifiant l'impot sur le revenu 
Troisieme lecture-Suite du debat 
Honorables senateurs, le regime fiscal actuel ne fonctionne plus. 
Il est complexe et injuste. 
Il fait obstacle a l'epargne, aux investissements et a la creation d'emplois. 
Je ne veux donc pas travailler plus.  
Le systeme fiscal serait beaucoup plus juste puisque tous seraient traites de la meme facon. 
De plus, cette taxe serait progressive. 
Les riches en profiteraient-ils? 
Quiconque gagne dix fois plus ne devrait pas payer le moins. 
Son impot sur le revenu serait dix fois superieur.  
(Sur la motion du senateur Kinsella, le debat est ajourne.)  
Projet de loi modificatif-Deuxieme lecture-Ajournement du debat 
Nous avons maintenant la possibilite d'appuyer ces modifications importantes. 
Le projet de loi C-79 part de ces dispositions et y ajoute certaines modifications. 
Permettez-moi d'expliquer brievement les dispositions.  
Une autre disposition importante concerne la securite des victimes. 
Grace aux modifications, il est fait expressement mention de la victime. 
La disposition du code precisera que le delinquant devra payer l'amende supplementaire. 
Mais en pratique, on decourage parfois les victimes de faire une declaration verbale.  
Elles ont besoin d'information. 
Elles ont besoin de securite. 
Elles ont besoin d'etre entendues et respectees. 
Les victimes doivent beneficier de ces modifications le plus tot possible.  
(Sur la motion du senateur Kinsella, au nom du senateur LeBreton, le debat est ajourne.)  
