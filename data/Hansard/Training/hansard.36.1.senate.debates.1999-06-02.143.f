Projet de loi modificatif-Ajournement du debat 
Il vise a ameliorer la securite de nos enfants et d'autres personnes vulnerables. 
Je me permets d'en faire l'historique pour les senateurs. 
Leur condamnation ne s'en trouve pas pour autant effacee, ni leur dossier vierge. 
La condamnation est un fait historique. 
C'est ce que le projet de loi C-69 nous aidera a faire.  
Ce projet de loi officialise des mesures qui ont deja ete prises. 
Plus de 700 000 recherches ont ete effectuees jusqu'a present.  
C'est exactement ce que vise la Loi sur le casier judiciaire. 
Dans la plupart des cas, effectivement, ces dossiers devraient etre invisibles. 
Ces recommandations ont toutes ete adoptees. 
Les ministres de la Justice de tous les gouvernements ont appuye ce principe. 
Il sera toujours necessaire d'obtenir le consentement du demandeur. 
Enfin, le solliciteur general devra convenir que la communication du dossier est justifiee. 
A l'heure actuelle, la revocation automatique ne s'applique qu'aux actes criminels. 
Honorables senateurs, ces modifications sont importantes. 
Elles s'appuient sur l'application anterieure de mesures efficaces deja instituees par le gouvernement. 
Elles ont recueilli l'appui unanime de tous les partis de l'autre endroit. 
Pour ces motifs, je crois que le projet de loi C-69 merite notre appui.  
