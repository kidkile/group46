Le financement de l'enseignement postsecondaire 
L'honorable Mabel M. DeWare :
Le budget d'hier s'inscrit dans cette demarche. 
Honorables senateurs, je porte un interet personnel aux questions relatives a l'enseignement postsecondaire. 
Il s'agissait d'annees difficiles. 
Il etait difficile de se nourrir et de se loger. 
Parfois, nous devions compter sur la generosite de nos familles. 
Il a eu une carriere longue et productive.  
Ce sont les etudiants qui doivent payer la note. 
Cette importance a deja ete bien documentee dans diverses etudes. 
Le comite senatorial special sur l'enseignement postsecondaire a effectue une etude de ce genre. 
Les emplois, les recettes fiscales et les retombees economiques disparaitront avec elles. 
La principale responsabilite d'offrir l'enseignement postsecondaire incombe aux gouvernements provinciaux. 
Toutefois, l'enseignement postsecondaire va dans le sens de l'interet national. 
Un solide engagement de ressources s'impose clairement de la part du gouvernement federal. 
L'accessibilite des universites en souffre egalement.  
Voila un chiffre bouleversant, honorables senateurs.  
Le financement supplementaire de la Fondation supplementaire pour l'innovation est bienvenu.  
