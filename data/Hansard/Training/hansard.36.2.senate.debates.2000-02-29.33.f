L'avenir de la politique du Canada en matiere de defense 
L'honorable J. Michael Forrestall , ayant donne avisle 22 fevrier 2000: 
Honorables senateurs, le Canada fait partie de la communaute mondiale. 
Il est membre du G-8. 
Cependant, nous avons des preoccupations regionales et nationales qui necessitent notre attention.  
Le Canada est aussi un membre de l'OTAN. 
Parce qu'il intervient dans l'economie mondiale, le Canada ne peut s'isoler.  
Il ne s'agit pas de remarques futiles. 
Sans trop aller dans les details, je dirai qu'il y a certaines preoccupations urgentes. 
Le budget des operations et de l'entretien devrait etre separe du budget des immobilisations. 
Les honorables senateurs comprendront donc pourquoi je suis tres decu aujourd'hui.  
(Sur la motion du senateur Hays, le debat est ajourne.)  
Energie, environnement et ressources naturelles 
L'honorable Mira Spivak , ayant donne avisle jeudi 24 fevrier 2000: 
Rapport du comite sur l'etude-Suite du debat 
Permission ayant ete accordee de revenir aux Rapports de comite: 
Comme les honorables senateurs le savent, j'ai ete vice-presidente du comite. 
Il est important aussi de par la methodologie utilisee pour en arriver a ces recommandations.  
Cette table ronde etait composee d'anciens, de chefs traditionnels et de meres de clan. 
Nous avons beaucoup beneficie de leurs conseils.  
Ces recommandations devraient etre vues comme etant inextricablement liees. 
On ne saurait considerer cela comme une methode satisfaisante de resoudre les questions en litige. 
Il faut donc faire preuve de flexibilite.  
De cette facon, le MAINC n'aurait plus a s'occuper de l'autonomie gouvernementale.  
La creation de cette commission comblerait un trou beant dans nos relations avec les autochtones. 
Si celui-ci refuse d'aider, il faut envisager un recours devant les tribunaux. 
La Charte des droits et libertes garantit les droits autochtones aux hommes comme aux femmes. 
Cependant, dans la realite, les choses se passent autrement.  
A mon avis, c'est un premier pas dans la bonne direction. 
Toutefois, le comite des peuples autochtones a encore beaucoup a faire. 
Ce sont la des volets cruciaux.  
(Sur la motion du senateur Pearson, le debat est ajourne.)  
