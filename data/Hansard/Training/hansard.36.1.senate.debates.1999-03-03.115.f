Le deces de Jack Webster 
Le journaliste de la Colombie-Britannique-Hommages 
L'honorable Raymond J. Perrault :
Jack etait un journaliste tres integre. 
Jack ne se faisait jamais d'opinion definitive sur quoi que ce fut. 
Comme interrogateur, il etait implacable. 
C'etait une experience stimulante.  
Jack Webster etait une perle rare du systeme de communications canadien. 
D'ascendance ecossaise, Jack etait ne dans le quartier ouvrier de Glasgow. 
Il avait emigre au Canada vers la fin des annees 1940. 
Le deces de Jack est une grande perte pour le monde des communications au Canada. 
Jack Webster nous manquera beaucoup a tous.  
C'etait au debut des annees 60. 
Jack avait une personnalite rafraichissante. 
Jack avait une maniere a nulle autre pareille de vous mettre au pied du mur. 
Je demissionnerai quand vous aurez demissionne de ce programme particulier. 
Jack avait une attitude constante dans la vie. 
Autrement dit, ramenons a nous ceux qui n'ont plus les deux pieds sur terre. 
Ramenons quelques cases en arriere ceux qui se pensent bons. 
Assurons-nous que l'humilite regne en politique, en affaires et au travail. 
Jack croyait que son role dans la vie etait de niveler tout cela.  
Jack va nous manquer. 
Comme le disait le senateur Perrault, c'etait un grand communicateur et un homme integre. 
