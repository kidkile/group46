Le quatre-vingt-deuxieme anniversaire de l'explosion de Halifax 
L'honorable J. Michael Forrestall :
Honorables senateurs, c'est en effet un jour triste pour le Canada. 
Aujourd'hui marque le quatre-vingt-deuxieme anniversaire de l'explosion de Halifax. 
Les faits entourant l'explosion sont bien connus. 
Quelque 13 500 batiments ont ete rases et 6 000 personnes sont demeurees sans abri.  
On se rappelle encore cette tragedie incroyable dans notre province. 
Une commemoration tres appropriee s'est deroulee au lever du soleil, ce matin, a Halifax.  
