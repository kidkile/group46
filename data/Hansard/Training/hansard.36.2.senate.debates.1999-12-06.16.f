L'honorable A. Raynell Andreychuk :
Honorables senateurs, j'appuie certainement les observations du senateur Milne. 
J'appuie les amendements. 
Je tiens cependant a signaler deux sujets d'inquietude. 
Je dois cependant exprimer certaines inquietudes concernant l'integrite du processus de rehabilitation.  
Pourtant, par inadvertance, tous les dossiers des conducteurs du Manitoba ont ete egares. 
Il serait inacceptable que le systeme de rehabilitation ne conserve pas son integrite. 
Ce qui m'inquiete, c'est que les systemes se deteriorent avec le temps. 
Ils ne figurent donc pas dans le systeme de casiers judiciaires.  
Dans pareils cas, une verification du casier judiciaire n'est guere utile.  
Les enfants seront proteges dans la mesure ou les processus de selection seront suivis.  
Ces deux observations etant faites, j'appuie le projet de loi ainsi modifie. 
Cependant, je deconseille au gouvernement d'intervenir dans le processus de rehabilitation. 
