Le dixieme anniversaire de la tragedie de l'Ecole polytechnique 
Nous avons modifie les lois. 
Honorables senateurs, nous sommes beaucoup trop lents a nous en occuper. 
L'honorable Erminie J. Cohen :
C'est aujourd'hui le dixieme anniversaire du massacre de Montreal.  
Le 6 decembre 1999 a ete un moment essentiel dans l'histoire du Canada. 
Malheureusement, la violence contre les femmes continue d'etre une realite au Canada. 
Ce sont des maris, des conjoints, des voisins et des meilleurs amis. 
Le statut social ou la situation economique importe peu.  
Les hommes et les femmes doivent s'elever contre cette situation honteuse. 
Ces jeunes femmes qui avaient toute leur vie a vivre meritaient beaucoup mieux que cela. 
Un nouveau phenomene est apparu: la violence dans les cours d'ecole. 
C'est un grave probleme que tous les Canadiens doivent contribuer a eliminer.  
Il n'est pas suffisant de nous souvenir de ces femmes. 
Des journees d'action sont organisees afin d'eliminer la violence faite aux femmes. 
Tout cela est extremement important. 
Honorables senateurs, nous commemorons aujourd'hui un anniversaire tragique et triste. 
En effet, le 6 decembre 1989, un homme, Marc Lepine, assassinait 14 femmes. 
Il les a tuees parce qu'elles etaient des femmes. 
L'Ecole polytechnique, a Montreal, a ete le theatre de ce drame epouvantable. 
Hier, un monument commemoratif a ete devoile a la memoire des victimes.  
Cette tragedie defie l'entendement. 
Nous devons poursuivre notre reflexion sur les causes profondes de cette violence. 
Chaque niveau de gouvernement, chacun dans sa sphere, doit intervenir. 
L'honorable Dan Hays (leader adjoint du gouvernement) :
Ce triste anniversaire doit nous donner a reflechir. 
Nous continuons a oeuvrer pour l'elimination de la violence faites aux femmes.  
Ce n'est pas accidentel. 
