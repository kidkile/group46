L'intervention en Yougoslavie-Les relations avec le droit international-Interpellation-Suspension du debat 
La guerre au Kosovo concernait essentiellement la primaute du droit. 
Je ne partage pas du tout ce point de vue. 
Le pape Jean-Paul II a deplore les souffrances humaines causees par les bombardements. 
Ici, au Canada, James Bissett, ancien ambassadeur du Canada en Yougoslavie, a dit: 
Honorables senateurs, examinons un instant ce qui s'est reellement passe. 
Ces bombardements ont fait13 000 victimes civiles, dont 2 500 morts. 
L'OTAN a persiste a bombarder parce que sa credibilite meme etait devenue un enjeu.  
Une nouvelle course aux armements s'est amorcee.  
Honorables senateurs, on a dit que la campagne de l'OTAN etait une guerre juste. 
Or, une guerre juste doit obeir a deux criteres: la limitation et la proportionnalite. 
Maintenant, on tue et endommage au hasard, comme l'a montre la situation au Kosovo. 
Quand les bombardements ont commence, 45 000 refugies kosovars avaient pris la fuite. 
Apres les premieres frappes, le nombre des refugies est passe a 855 000. 
Les bombardements ont aggrave la situation.  
Bien sur qu'il faut faire quelque chose. 
Nous avons perdu de vue hier ce fait fondamental. 
Heureusement que nous avons les Nations Unies. 
Le Canada tente encore d'equilibrer son adhesion aux Nations Unies et a l'OTAN. 
Le Canada se trouve face a un dilemme. 
Bien entendu, les autres membres de l'OTAN ont fait la meme chose. 
Ils ont tous altere le droit international par la guerre.  
Toutefois, la necessite de ces appareils, devant la puissance ecrasante des Etats-Unis, etait minime. 
