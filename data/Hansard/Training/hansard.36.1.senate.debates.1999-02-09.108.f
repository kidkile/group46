Le sort des sans-abri 
L'honorable Erminie J. Cohen :
On a retrouve Al mort.  
Cet incident a change notre perception des gens qui vivent dans nos rues. 
Sont-ils sales et loqueteux? 
N'ont-ils plus aucune ambition ni aucun espoir dans la vie? 
Il est devenu trop facile de parler des sans-abri. 
Nous ne sommes plus sensibles a ce que cela signifie. 
Ils n'ont pas de lit le soir et n'ont aucun endroit ou cuisiner. 
Leurs amis et les membres de leur famille ne peuvent venir les visiter. 
Les nuits particulierement froides, les travailleurs de rue doivent leur remettre des sacs de couchage. 
C'est une vie privee de toute dignite et de tout controle.  
