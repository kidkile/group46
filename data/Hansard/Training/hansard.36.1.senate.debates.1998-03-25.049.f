La loi sur les prets aux petites entreprises 
Projet de loi modificatif-Deuxieme lecture 
Apres avoir serieusement reflechi, j'ai decide de voter en faveur de cette loi. 
Honorables senateurs, les petites entreprises jouent un role vital dans l'economie du Canada. 
En 1995-1996, par exemple, plus de 30 000 entreprises ont eu recours au programme. 
Le programme connaitrait ainsi une fin desordonnee. 
Cela lui permet de reduire les risques mais ne les elimine pas pour les preteurs. 
En terminant, j'aimerais revenir au rapport du verificateur general. 
Il convient ici, selon moi, de rappeler trois points en particulier.  
S'il faut modifier le programme, on pourra le faire pendant l'examen detaille. 
L'honorable Pierre Claude Nolin :
Je ne suis pas presidente du comite qui examine ces questions. 
Nous arrivons d'une consultation sur la nouvelle societe qui gerera les fonds de pension.  
L'honorable Duncan J. Jessiman :
Mais j'en parlerai plus tard.  
Cette loi a ete presentee en 1961 par le gouvernement Diefenbaker. 
Les prets consentis de 1961 a 1980 etaient modestes. 
Les resultats ont ete exceptionnels. 
Les exceptions sont les fermes et les organisations existant a des fins caritatives ou religieuses. 
Ce programme souleve deux preoccupations. 
Le montant est considerable. 
Toutefois, le plafond des prets reste de 250 000 $. 
Le chiffre est important, car l'emprunteur est directement touche. 
Par consequent, 4,400 millions de dollars creent30 800 emplois. 
Il ne fait aucun doute que le gouvernement pourrait prendre d'autres mesures. 
Ces mesures encourageraient la petite entreprise.  
Il parviendra au comite demain. 
Dans seulement six jours,le 31 mars, la periode de pret expirera. 
Ce n'est tout simplement pas assez.  
Il y a une etude en cours. 
Le verificateur general nous a suggere d'examiner plusieurs aspects. 
C'est une mesure importante. 
Son Honneur le President :
Plait-il aux honorables senateurs d'adopter la motion?  
(La motion est adoptee et le projet de loi est lu une deuxieme fois.)  
Son Honneur le President :
Honorables senateurs, quand lirons-nous le projet de loi une troisieme fois?  
