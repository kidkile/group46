L'histoire des Chinois au Canada 
L'honorable Vivienne Poy , ayant donne avis le 8 decembre 1998: 
C'est l'histoire des Chinois canadiens. 
Le groupe se composait de 50 a 70 manoeuvres et charpentiers. 
Ils arriverent a la baie Nookta, dans l'ile de Vancouver, au debut juin. 
Le sort des charpentiers chinois resta un mystere. 
Selon certains rapports, ils auraient ete captures par les Espagnols et emmenes au Mexique. 
Quoi qu'il en soit, apres une generation ou deux, on avait perdu leurs traces. 
Jusqu'a90 p. 100 des paysans perdirent leurs terres. 
Toutefois, les enlevements se poursuivirent.  
Au cours des huit premiers mois de 1850, 50 000 Chinois immigrerent en Californie. 
De nombreux Chinois se lancerent dans le secteur des services dans les villes minieres. 
Victoria devint le principal centre d'immigration pour les Chinois arrivant en Amerique du Nord.  
En 1860, le Times de Londres ecrivait que: 
... c'est-a-dire contre les Chinois...  
Un article publie en 1861 dans le Victoria British Colonist disait ceci: 
Le 20 juillet 1871, la Colombie-Britannique devient une province canadienne. 
La premiere societe antichinoise a vu le jour en mai 1873, a Victoria.  
Le chef de l'opposition, Alexander MacKenzie, a affirme que 
Les travailleurs chinois recevaient la moitie du salaire des Blancs. 
Sir Matthew Begbie, juge en chef de la Colombie-Britannique, a dit: 
Je ne vois pas comment la population s'en tirerait sans les Chinois. 
En 1883, le premier ministre Macdonald a declare franchement a la Chambre des communes: 
Beaucoup de gens ont perdu la vie en construisant le chemin de fer. 
Ainsi, deux travailleurs chinois sont morts pour chaque mille de chemin de fer. 
Les conditions de vie etaient si mauvaises que les Chinois ne recevaient aucun traitement medical. 
Bon nombre sont partis vers les Prairies et l'est du Canada. 
Un millier d'entre eux sont retournes en Chine. 
La plupart sont demeures en Colombie-Britannique. 
En 1900, cet impot a ete hausse a 100 $. 
En 1895, la chambre de commerce chinoise etait creee a Vancouver.  
En 1907, des emeutes antiasiatiques se sont repandues dans tout le quartier chinois de Vancouver. 
Ceux qui ont pris la parole ont reclame un Canada de race blanche. 
Peu importe ou les Chinois sont alles, ils ont toujours ete victime de discrimination. 
Permettez-moi maintenant d'aborder la Loi de l'immigration chinoise de 1923. 
Au cours de la Seconde Guerre mondiale, 500 Chinois ont servi dans l'armee canadienne. 
Lorsque la Seconde Guerre mondiale a eclate, Jung et ses freres se sont enroles. 
