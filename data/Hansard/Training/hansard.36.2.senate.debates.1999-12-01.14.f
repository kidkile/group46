La Loi de mise en oeuvre de l'Accord sur la Station spatiale internationale civile 
L'honorable William M. Kelly :
L'industrie spatiale du Canada est devenue un rouage important de l'economie canadienne. 
J'espere que ce n'est pas ce qui se passe.  
J'ai dit plus tot que cette initiative meritait, a mon avis, notre soutien enthousiaste. 
La Station spatiale internationale n'a aucun role militaire direct. 
Par exemple, elle ne pourra pas servir a lancer des satellites ou des missiles. 
Cela garantit qu'elle servira uniquement a des fins pacifiques.  
L'experience russe, avec la station spatiale MIR, represente d'enormes avantages pour le projet. 
Les fusees russes Proton et Soyouz sont aussi des elements importants du projet. 
