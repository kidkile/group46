Le president d'election (M. Caccia):
Je tiens a vous remercier de la confiance que vous venez de me temoigner. 
Je suis a la fois tres reconnaissant et profondement touche.  
Pourquoi n'y aurait-il pas beaucoup d'action? 
Vous allez defendre comme il se doit des opinions bien arretees.  
Nos armes sont nos paroles, nos idees, nos convictions. 
Voila pourquoi il y a de l'action.  
D'une maniere generale, nous avons, vous et moi, trois droits et privileges fondamentaux.  
Nous avons le droit de venir a cet endroit. 
Nous avons le droit de parler sans crainte, et celui de voter. 
Je suis, comme vous le savez, au service de la Chambre. 
Et maintenant, le Parlement peut se mettre au travail. 
Je vous remercie beaucoup.  
Et la masse ayant ete posee sur la table: 
M. John Nunziata (York-Sud-Weston, Ind.):
Je vous considere comme un ami. 
